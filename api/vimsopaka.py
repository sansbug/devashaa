# ⚠ SAPTAVARGA — PARTIALLY UNRECOVERABLE. THE MODULE TAKES NO POSITION.
#
# The śloka prints seven figures, "5, 2, 3,2t,412, 2 and 1". Two are OCR
# damage: "2t" is 2½ and "412" is 4½ (they are the only readings that let the
# column total 20, and 2½ and 4½ are the two values Santhanam's table column
# carries). So the MULTISET is certain: {5, 2, 3, 2.5, 4.5, 2, 1} → 20.0.
# Rāśi 5, Horā 2 and Drekkāṇa 3 are also certain — they are the first three of
# the śloka's list and the list's head is undamaged.
#
# What is NOT certain is which of Navāṁśa / Dvādaśāṁśa / Triṁśāṁśa / Saptāṁśa
# takes which of {2.5, 4.5, 2, 1}:
#
#   READING A "sloka_order" — take the list in the order the sentence builds
#   it: the Ṣaḍvarga sequence, with Saptāṁśa appended last, since vv.17-19 say
#   "Adding the Sapthamamsa to the Shad Vargas". Gives Navāṁśa 2.5,
#   Dvādaśāṁśa 4.5, Triṁśāṁśa 2, Saptāṁśa 1.
#
#   READING B "ascending_divisor" — take the list in ascending-divisor order,
#   which is where Saptāṁśa sits in every other varga enumeration. Gives
#   Saptāṁśa 2.5, Navāṁśa 4.5, Dvādaśāṁśa 2, Triṁśāṁśa 1.
#
# THE EVIDENCE POINTS BOTH WAYS AND NEITHER ARGUMENT IS DECISIVE.
#
#   For A — the table fragment. Santhanam's Saptha Varga column survives
#   extraction as the run "3.0, 2.5, 4.5, 20.0, 5.0, 2.0, 2.0, 1.0"
#   (vol1.txt lines 3881-3888), i.e. two blocks split at the 20.0 total. The
#   extractor demonstrably preserves ROW ORDER inside a column run: the Shad
#   Varga column comes out "6 2 4 5 2 I" — rows 1-6 in order — and the
#   Daśa/Ṣoḍaśa pair comes out row by row from Rāśi to the Total. Under A the
#   run maps to rows (3,4,5,Total)(1,2,6,7): both blocks ascending. Under B it
#   maps to rows (3,7,4,Total)(1,2,5,6): the first block is out of order. So
#   the surviving table evidence FAVOURS A, and A is also the śloka's literal
#   word order. It is weak evidence — this is the one column the extractor
#   fragmented — but it is the only table evidence there is.
#
#   For B — column monotonicity. In TWO of the three undamaged columns
#   Navāṁśa outweighs Dvādaśāṁśa (Ṣaḍvarga 5 vs 2, Ṣoḍaśavarga 3.0 vs 0.5);
#   the Daśavarga column has them EQUAL at 1.5 vs 1.5 and is therefore silent
#   on the point. Reading A inverts the relation, B preserves it. B also makes
#   the Ṣaḍvarga → Saptavarga transition tidy: Rāśi 6→5, Drekkāṇa 4→3 and
#   Navāṁśa 5→4.5 give up exactly 2.5 between them to fund the new Saptāṁśa
#   2.5, leaving Horā, Dvādaśāṁśa and Triṁśāṁśa untouched.
#
# Those are arguments, not readings. The text does not settle it, this module
# recommends NEITHER, and it REFUSES the Saptavarga scheme unless the caller
# names a reading and thereby owns the choice. The permanent fix is a clean
# scan of the Saptha Varga column of the table on Vol I book p.97.
# ---------------------------------------------------------------------------

