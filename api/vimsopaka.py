def swaviswa(scheme: str, saptavarga_reading: str | None = None) -> dict[str, float]:
    """The Swaviśva column for a scheme, as {varga_key: points}.

    Raises for `scheme="saptavarga"` unless a reading is named.
    """
    if scheme not in SCHEMES:
        raise ValueError(f"unknown scheme {scheme!r}; expected one of {sorted(SCHEMES)}")
    if scheme == "saptavarga":
        if saptavarga_reading is None:
            raise ValueError(SAPTAVARGA_UNAVAILABLE)
        if saptavarga_reading not in SAPTAVARGA_READINGS:
            raise ValueError(
                f"unknown saptavarga_reading {saptavarga_reading!r}; "
                f"expected one of {sorted(SAPTAVARGA_READINGS)}"
            )
        return dict(SAPTAVARGA_READINGS[saptavarga_reading])
    return dict(_SWAVISWA[scheme])


