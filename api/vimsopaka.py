VARGA_VISWA: dict[str, float] = {
    "own": 20.0,          # swakṣetra — the graha is itself the varga lord
    "adhimitra": 18.0,    # extreme friend
    "mitra": 15.0,        # friend
    "sama": 10.0,         # equal / neutral
    "satru": 7.0,         # enemy
    "adhisatru": 5.0,     # sworn (extreme) enemy
}

# Ladder order, strongest first. Useful to a UI and to anything that wants to
# reason about "one rung down" without hardcoding the sequence again.
RUNGS: tuple[str, ...] = ("own", "adhimitra", "mitra", "sama", "satru", "adhisatru")

