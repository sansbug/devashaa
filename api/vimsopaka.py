# ---------------------------------------------------------------------------
# The computation — vv.26-27: "Multiply the figure due to full strength for
# the division by the [Varga Viswa and divide] by 20".
# ---------------------------------------------------------------------------

def vimsopaka(scheme: str,
              rungs: dict[str, str],
              saptavarga_reading: str | None = None) -> dict:
    """Viṁśopaka bala for one graha under one varga scheme.

    `rungs` maps each varga key of the scheme to a key of VARGA_VISWA — the
    compound relationship of the graha with that division's lord ("own" when
    the graha rules the division itself). Supplying it is the caller's job
    because the compound relationship needs ch.3 v.56's temporal half, which
    depends on the whole chart, not on this graha's longitude alone. See
    `natural_relation_rungs()` for a weaker, chart-free fallback.

    Returns the per-varga working as well as the total, so a UI can show the
    same table the book prints on book p.99 rather than a bare number.
    """
    sv = swaviswa(scheme, saptavarga_reading)

    missing = [k for k in SCHEMES[scheme] if k not in rungs]
    if missing:
        raise ValueError(f"no rung given for {missing} in scheme {scheme!r}")

    rows, total = [], 0.0
    for key in SCHEMES[scheme]:
        rung = rungs[key]
        if rung not in VARGA_VISWA:
            raise ValueError(
                f"varga {key}: unknown rung {rung!r}; expected one of {list(RUNGS)}"
            )
        vv = VARGA_VISWA[rung]
        pts = sv[key] * vv / TOTAL
        total += pts
        rows.append({
            "varga": key,
            "swaviswa": sv[key],
            "relationship": rung,
            "varga_viswa": vv,
            "points": round(pts, 6),
        })

    total = round(total, 6)
    out = {
        "scheme": scheme,
        "saptavarga_reading": saptavarga_reading if scheme == "saptavarga" else None,
        "rows": rows,
        "points": total,
        "maximum": TOTAL,
        "verdict": verdict(total),
        "grade": grade(total),
        "citation": "BPHS Vol I ch.7 vv.17-27",
    }
    if scheme == "saptavarga":
        # The score is only as good as the reading the caller chose. Say so in
        # the payload itself, so provenance cannot be lost downstream.
        out["saptavarga_ambiguous"] = list(SAPTAVARGA_AMBIGUOUS)
        out["saptavarga_unavailable_note"] = SAPTAVARGA_UNAVAILABLE
        out["saptavarga_reading_evidence"] = SAPTAVARGA_READING_EVIDENCE[
            saptavarga_reading
        ]
    return out


# ---------------------------------------------------------------------------
# vv.26-27 verdict bands. MŪLA TEXT — these four are the sage's own.
#
# The śloka's boundaries are open: "below 5", "above 5 but below [10]", "upto
# [15]", "above 15". It says nothing about a score sitting exactly on 5 or
# exactly on 10, so those points are a genuine gap; we take each band as
# closed below and open above, and flag it rather than pretend otherwise.
# ---------------------------------------------------------------------------

VERDICT_BANDS: tuple[tuple[float, float, str], ...] = (
    (0.0,  5.0,  "not capable of giving auspicious results"),
    (5.0,  10.0, "yields some good effects"),
    (10.0, 15.0, "indicative of mediocre effect"),
    (15.0, 20.0, "yields wholly favourable effects"),
)

VERDICT_BOUNDARIES_OPEN = (
    "vv.26-27 read 'below 5', 'above 5 but below [10]', 'upto [15]', 'above "
    "15'. A score of exactly 5.0, 10.0 or 15.0 is not addressed by the sloka. "
    "Each band here is taken as [low, high) — lower bound inclusive."
)

_VERDICT_OPEN_POINTS = frozenset({5.0, 10.0, 15.0})


def verdict(points: float) -> dict:
    """The śloka's own four-fold reading of a viṁśopaka total.

    Refuses a negative score rather than falling through to a band. A
    viṁśopaka total cannot be negative (every Swaviśva weight and every rung
    is positive), so a negative argument means the caller's arithmetic is
    wrong, and the honest answer is an error, not the top band.
    """
    if not points >= 0.0:            # also catches NaN
        raise ValueError(
            f"vimsopaka points must be >= 0 (0-{TOTAL} scale); got {points!r}"
        )
    for low, high, text in VERDICT_BANDS:
        if low <= points < high:
            break
    else:                                   # exactly 20.0, or above
        low, high, text = VERDICT_BANDS[-1]
    out = {
        "from": low,
        "to": high,
        "effect": text,
        "source": "sloka",
        "citation": "BPHS Vol I ch.7 vv.26-27",
    }
    if points in _VERDICT_OPEN_POINTS:
        out["boundary_open"] = VERDICT_BOUNDARIES_OPEN
    return out


# ---------------------------------------------------------------------------
# vv.30-32 grades. The eight NAMES are Parāśara's — see the module docstring
# for the verbatim text and its repairs.
#
# THE NUMERIC BANDS ARE NOT. They are Santhanam's, and he says so plainly:
#   VERBATIM (vol1 pdf p.100):
#     ". The perceDtages given by me are numericat assignments fora
#      comparative study so that an idea is gained or the gmdations
#      originally suggested;"
#   READING:
#     "The perce[n]tages given by me are numerica[l] assignments fo[r a]
#      comparative study so that an idea is gained o[f] the g[ra]dations
#      originally suggested."
#
# His table (book p.101 = pdf p.100) is eight equal 2.5-point bands over 0-20,
# with the percentage being simply points/20. Both are derived here, not typed.
# ---------------------------------------------------------------------------

# Ascending, weakest first — the order Santhanam's bands run in, not the order
# the śloka lists the names in.
GRADE_NAMES: tuple[str, ...] = (
    "Atiheena", "Heena", "Atiswalpa", "Swalpa",
    "Madhya", "Atimadhya", "Poorna", "Atipoorna",
)
GRADE_BAND = TOTAL / len(GRADE_NAMES)      # 2.5 points

GRADE_BANDS_ARE_SANTHANAMS_NOTE = (
    "BPHS Vol I ch.7 vv.30-32 give the eight names only. The 2.5-point bands "
    "and the percentages are R. Santhanam's note, expressly offered by him as "
    "'numerica[l] assignments fo[r a] comparative study'."
)

GRADE_BOUNDARIES_OPEN = (
    "Santhanam's printed grade table (book p.101 = pdf p.100) states its bands "
    "as '17.5 to 20.0', '15.0 to 17.5', '12.5 to 15.0' and so on, so the bands "
    "OVERLAP at every 2.5 mark: 17.5 is both the top of Poorna and the bottom "
    "of Atipoorna. The table does not say which band owns the shared point. "
    "grade() resolves such a score UPWARD, into the stronger band — a choice, "
    "not a reading. This is the grade-table analogue of "
    "VERDICT_BOUNDARIES_OPEN."
)


def grade(points: float) -> dict:
    """Santhanam's eight-fold grade for a viṁśopaka total. NOT mūla text."""
    if not points >= 0.0:            # also catches NaN; same rule as verdict()
        raise ValueError(
            f"vimsopaka points must be >= 0 (0-{TOTAL} scale); got {points!r}"
        )
    idx = int(points // GRADE_BAND)
    idx = max(0, min(len(GRADE_NAMES) - 1, idx))
    low = idx * GRADE_BAND
    out = {
        "name": GRADE_NAMES[idx],
        "from": round(low, 6),
        "to": round(low + GRADE_BAND, 6),
        "percent": round(100.0 * points / TOTAL, 6),
        "percent_from": round(100.0 * low / TOTAL, 6),
        "percent_to": round(100.0 * (low + GRADE_BAND) / TOTAL, 6),
        "source": "note",
        "citation": "R. Santhanam's note to BPHS Vol I ch.7 vv.30-32",
    }
    # A score sitting exactly on an interior 2.5 mark is claimed by two of
    # Santhanam's printed bands. Say so instead of resolving it silently.
    if 0.0 < points < TOTAL and points % GRADE_BAND == 0.0:
        out["boundary_open"] = GRADE_BOUNDARIES_OPEN
    return out


def grade_table() -> list[dict]:
    """Santhanam's whole grade table, weakest band first."""
    return [grade(i * GRADE_BAND) for i in range(len(GRADE_NAMES))]


# ---------------------------------------------------------------------------
# FALLBACK — natural relationships only.
#
# The real ladder wants the compound relationship (ch.3 vv.57-58): natural
# (v.55) folded with temporal (v.56). dignity.py has only the natural half, so
# this fallback can produce own / mitra / sama / satru but NEVER adhimitra or
# adhisatru — both of those require agreement between the two halves.
#
# The consequence is not cosmetic. Santhanam's own worked example (book p.99 =
# pdf p.98) scores Venus at five Adhimitra vargas and one Adhisatru; under this
# fallback those become Mitra and Satru, and the total drops from 16.80 to
# 14.45 — across the śloka's 15-point boundary, i.e. from "wholly favourable"
# to "mediocre". Use this only when the temporal half is genuinely unavailable,
# and say which you used. If api/relationships.py is available, use it instead
# (see the integration note in the module docstring).
# ---------------------------------------------------------------------------

NATURAL_FALLBACK_IS_WEAKER = (
    "natural_relation_rungs() uses ch.3 v.55 natural relations only. It cannot "
    "return 'adhimitra' or 'adhisatru', which need the temporal relation of "
    "ch.3 v.56 folded in per ch.3 vv.57-58. Totals from it are systematically "
    "compressed toward the middle of the 0-20 range."
)

# Natural five-fold -> ladder rung. dignity.py's vocabulary on the left.
_NATURAL_TO_RUNG: dict[str, str] = {
    "friend": "mitra",
    "neutral": "sama",
    "enemy": "satru",
}

NODES_UNSUPPORTED = (
    "BPHS gives Rahu and Ketu no rasi lordship and no exaltation, so dignity.py "
    "returns them no natural relations and no varga-viswa rung can be derived "
    "for them from it. natural_relation_rungs() refuses the nodes rather than "
    "inventing a relationship."
)


def natural_relation_rungs(graha: str, varga_signs: dict[str, int]) -> dict[str, str]:
    """Per-varga ladder rungs from natural relations alone — the weak fallback.

    `varga_signs` maps varga keys to the sign index 0-11 the graha occupies in
    that division (exactly what `vargas.all_vargas()` returns). Import is done
    here rather than at module scope so that the rest of this module stays
    dependency-free and importable on its own.
    """
    from dignity import RASI_LORD, NATURAL_RELATIONS

    if graha not in NATURAL_RELATIONS:
        raise ValueError(f"{graha}: {NODES_UNSUPPORTED}")

    out = {}
    for key, sign in varga_signs.items():
        lord = RASI_LORD[sign % 12]
        if lord == graha:
            out[key] = "own"
        else:
            out[key] = _NATURAL_TO_RUNG[NATURAL_RELATIONS[graha][lord]]
    return out
